def Mês(M):
    if M == 1:
        return "Janeiro"
    elif M == 2:
        return "Fevereiro"
    elif M == 3:
        return "Março"
    elif M == 4:
        return "Abril"
    elif M == 5:
        return "Maio"
    elif M == 6:
        return "Junho"
    elif M == 7:
        return "Julho"
    elif M == 8:
        return "Agosto"
    elif M == 9:
        return "Setembro"
    elif M == 10:
        return "Outubro"
    elif M == 11:
        return "Novembro"
    elif M == 1:
        return "Dezembro"
    else:
        return "NaM" # Not a Month